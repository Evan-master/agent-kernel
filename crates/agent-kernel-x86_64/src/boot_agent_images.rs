#[derive(Copy, Clone)]
pub(super) struct BootAgentImage {
    bytes: &'static [u8],
    digest: AgentImageDigest,
    nonce: u64,
    result: TaskResult,
    expected_operations: [AgentCallOperation; 5],
    expected_return_offsets: [u32; 5],
}

#[derive(Copy, Clone)]
pub(super) struct BootVerifierImage {
    bytes: &'static [u8],
    digest: AgentImageDigest,
    nonce: u64,
    target: u64,
    result: TaskResult,
}

impl BootAgentImage {
    pub(super) const fn bytes(self) -> &'static [u8] {
        self.bytes
    }

    pub(super) const fn digest(self) -> AgentImageDigest {
        self.digest
    }

    pub(super) const fn nonce(self) -> u64 {
        self.nonce
    }

    pub(super) const fn result(self) -> TaskResult {
        self.result
    }

    pub(super) const fn expected_return_offsets(self) -> [u32; 5] {
        self.expected_return_offsets
    }

    pub(super) const fn expected_operations(self) -> [AgentCallOperation; 5] {
        self.expected_operations
    }
}

impl BootVerifierImage {
    pub(super) const fn bytes(self) -> &'static [u8] {
        self.bytes
    }

    pub(super) const fn digest(self) -> AgentImageDigest {
        self.digest
    }

    pub(super) const fn nonce(self) -> u64 {
        self.nonce
    }

    pub(super) const fn target(self) -> u64 {
        self.target
    }

    pub(super) const fn result(self) -> TaskResult {
        self.result
    }

    pub(super) const fn expected_return_offsets(self) -> [u32; 4] {
        [
            VERIFIER_DESCRIBE_RETURN_OFFSET,
            VERIFIER_INSPECTION_RETURN_OFFSET,
            VERIFIER_VERIFICATION_RETURN_OFFSET,
            VERIFIER_COMPLETION_RETURN_OFFSET,
        ]
    }

    pub(super) const fn expected_operations(self) -> [AgentCallOperation; 4] {
        VERIFIER_OPERATIONS
    }
}

